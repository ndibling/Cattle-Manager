    public void ExportHerdToCsv(IReadOnlyList<AnimalDto> animals, string filePath)
    {
        var config = new CsvConfiguration(CultureInfo.InvariantCulture)
        {
            HasHeaderRecord = true
        };
        using var writer = new StreamWriter(filePath);
        using var csv = new CsvWriter(writer, config);

        csv.WriteHeader<CsvAnimalRow>();
        csv.NextRecord();
        foreach (var a in animals)
        {
            csv.WriteRecord(new CsvAnimalRow(a));
            csv.NextRecord();
        }
    }

    public void ExportAnimalProfileToPdf(AnimalDto animal, string filePath)
    {
        Document.Create(container =>
        {
            container.Page(page =>
            {
                page.Size(PageSizes.A4);
                page.Margin(2, Unit.Centimetre);
                page.DefaultTextStyle(x => x.FontSize(11));

                page.Header().Text($"Herd Master — Animal Profile")
                    .SemiBold().FontSize(18).FontColor(Colors.Green.Darken3);

                page.Content().Column(col =>
                {
                    col.Spacing(10);

                    col.Item().Text($"{animal.BarnName}").Bold().FontSize(20);
                    if (!string.IsNullOrEmpty(animal.RegisteredName))
                        col.Item().Text($"Registered: {animal.RegisteredName}").Italic();

                    col.Item().LineHorizontal(1).LineColor(Colors.Grey.Lighten2);

                    col.Item().Row(row =>
                    {
                        row.RelativeItem().Column(c =>
                        {
                            c.Item().Text("Basic Information").Bold().FontSize(13);
                            c.Item().Text($"Breed: {animal.BreedName}");
                            c.Item().Text($"Gender: {animal.Gender}");
                            c.Item().Text($"Status: {animal.Status}");
                            c.Item().Text($"Birth Date: {animal.BirthDate:MMMM d, yyyy}");
                            c.Item().Text($"Age: {animal.AgeDisplay}");
                        });
                        row.RelativeItem().Column(c =>
                        {
                            c.Item().Text("Physical").Bold().FontSize(13);
                            c.Item().Text($"Weight: {animal.WeightDisplay}");
                            c.Item().Text($"Height: {animal.HeightDisplay}");
                            c.Item().Text($"Coloring: {animal.Coloring}");
                        });
                    });

                    col.Item().LineHorizontal(1).LineColor(Colors.Grey.Lighten2);

                    col.Item().Row(row =>
                    {
                        row.RelativeItem().Column(c =>
                        {
                            c.Item().Text("Health & Husbandry").Bold().FontSize(13);
                            c.Item().Text($"Last Worming: {animal.LastWormingDate?.ToString("MM/dd/yyyy") ?? "Not recorded"}");
                            c.Item().Text($"Last Vaccination: {animal.LastVaccinationDate?.ToString("MM/dd/yyyy") ?? "Not recorded"}");
                            c.Item().Text($"Last Health Check: {animal.LastHealthCheckDate?.ToString("MM/dd/yyyy") ?? "Not recorded"}");
                        });
                        row.RelativeItem().Column(c =>
                        {
                            c.Item().Text("Lineage").Bold().FontSize(13);
                            c.Item().Text($"Sire: {animal.SireBarnName ?? animal.ExternalSireName ?? "Unknown"}");
                            c.Item().Text($"Dam: {animal.DamBarnName ?? animal.ExternalDamName ?? "Unknown"}");
                        });
                    });

                    if (!string.IsNullOrEmpty(animal.HealthNotes))
                    {
                        col.Item().LineHorizontal(1).LineColor(Colors.Grey.Lighten2);
                        col.Item().Text("Health Notes").Bold();
                        col.Item().Text(animal.HealthNotes);
                    }
                });

                page.Footer().AlignCenter()
                    .Text(x =>
                    {
                        x.Span("Generated by Herd Master — ");
                        x.Span(DateTime.Now.ToString("MM/dd/yyyy HH:mm"));
                    });
            });
        }).GeneratePdf(filePath);
    }

