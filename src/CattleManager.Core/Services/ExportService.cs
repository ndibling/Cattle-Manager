    public void ExportBillOfSaleToPdf(TransactionDto sale, AnimalDto? animal, string farmName, string path)
    {
        Document.Create(c => c.Page(page =>
        {
            page.Size(PageSizes.A4);
            page.Margin(3, Unit.Centimetre);
            page.DefaultTextStyle(x => x.FontSize(11));
            page.Header().Column(col =>
            {
                col.Item().Text("BILL OF SALE")
                    .Bold().FontSize(22).FontColor(Colors.Green.Darken3).AlignCenter();
                col.Item().Text(farmName).FontSize(13).AlignCenter();
            });
            page.Content().Column(col =>
            {
                col.Spacing(10);
                col.Item().PaddingTop(10).LineHorizontal(1).LineColor(Colors.Grey.Lighten2);

                col.Item().Row(row =>
                {
                    row.RelativeItem().Column(c =>
                    {
                        c.Item().Text("SELLER").Bold();
                        c.Item().Text(farmName);
                    });
                    row.RelativeItem().Column(c =>
                    {
                        c.Item().Text("DATE OF SALE").Bold();
                        c.Item().Text(sale.Date.ToString("MMMM d, yyyy"));
                    });
                });

                if (animal is not null)
                {
                    col.Item().LineHorizontal(0.5f).LineColor(Colors.Grey.Lighten2);
                    col.Item().Text("ANIMAL DESCRIPTION").Bold().FontSize(12);
                    col.Item().Row(row =>
                    {
                        row.RelativeItem().Column(c =>
                        {
                            c.Item().Text($"Name: {animal.BarnName}");
                            if (!string.IsNullOrEmpty(animal.RegisteredName))
                                c.Item().Text($"Registered: {animal.RegisteredName}");
                            c.Item().Text($"Breed: {animal.BreedName}");
                            c.Item().Text($"Gender: {animal.Gender}");
                        });
                        row.RelativeItem().Column(c =>
                        {
                            c.Item().Text($"Birth Date: {animal.BirthDate:MM/dd/yyyy}");
                            if (!string.IsNullOrEmpty(animal.TagNumber))
                                c.Item().Text($"Tag No.: {animal.TagNumber}");
                        });
                    });
                }

                col.Item().LineHorizontal(0.5f).LineColor(Colors.Grey.Lighten2);
                col.Item().Text("TRANSACTION DETAILS").Bold().FontSize(12);
                AddLineRow(col, sale.Description, sale.Amount, true, fontSize: 13);
                if (!string.IsNullOrEmpty(sale.PayeePayer))
                    col.Item().Text($"Buyer: {sale.PayeePayer}");
                if (!string.IsNullOrEmpty(sale.PaymentMethod))
                    col.Item().Text($"Payment Method: {sale.PaymentMethod}");

                col.Item().PaddingTop(20).LineHorizontal(0.5f).LineColor(Colors.Grey.Lighten2);
                col.Item().Row(row =>
                {
                    row.RelativeItem().Column(c =>
                    {
                        c.Item().PaddingTop(30).LineHorizontal(0.5f).LineColor(Colors.Black);
                        c.Item().Text("Seller Signature / Date");
                    });
                    row.ConstantItem(20);
                    row.RelativeItem().Column(c =>
                    {
                        c.Item().PaddingTop(30).LineHorizontal(0.5f).LineColor(Colors.Black);
                        c.Item().Text("Buyer Signature / Date");
                    });
                });
            });
            page.Footer().AlignCenter().Text(x =>
            {
                x.Span("Generated by Herd Master — ");
                x.Span(DateTime.Now.ToString("MM/dd/yyyy HH:mm"));
            });
        })).GeneratePdf(path);
    }

    private static void AddLineRow(ColumnDescriptor col, string label, decimal amount,
        bool bold, bool italic = false, float fontSize = 10)
    {
        col.Item().Row(row =>
        {
            var labelText = row.RelativeItem().Text(label);
            var amountText = row.ConstantItem(110).Text(amount.ToString("C")).AlignRight();
            if (bold)   { labelText.Bold();   amountText.Bold(); }
            if (italic) { labelText.Italic(); amountText.Italic(); }
            if (fontSize != 10) { labelText.FontSize(fontSize); amountText.FontSize(fontSize); }
        });
    }

    private record CsvAnimalRow(AnimalDto A)
    {
        public string BarnName => A.BarnName;
        public string? RegisteredName => A.RegisteredName;
        public string Breed => A.BreedName;
        public string Gender => A.Gender.ToString();
        public string Status => A.Status.ToString();
        public string BirthDate => A.BirthDate.ToString("MM/dd/yyyy");
        public string Age => A.AgeDisplay;
        public string Weight => A.WeightDisplay;
        public string Height => A.HeightDisplay;
        public string? Coloring => A.Coloring;
        public string? Location => A.CurrentLocation;
        public string? LastWorming => A.LastWormingDate?.ToString("MM/dd/yyyy");
        public string? LastVaccination => A.LastVaccinationDate?.ToString("MM/dd/yyyy");
        public string? Sire => A.SireBarnName ?? A.ExternalSireName;
        public string? Dam => A.DamBarnName ?? A.ExternalDamName;
    }
