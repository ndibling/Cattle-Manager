    public void ExportTaxSummaryToPdf(TaxSummaryDto tax, string path)
    {
        Document.Create(c => c.Page(page =>
        {
            page.Size(PageSizes.A4);
            page.Margin(2, Unit.Centimetre);
            page.DefaultTextStyle(x => x.FontSize(10));
            page.Header().Text($"Herd Master — Tax Summary {tax.FiscalYear}")
                .SemiBold().FontSize(16).FontColor(Colors.Green.Darken3);
            page.Content().Column(col =>
            {
                col.Spacing(5);
                col.Item().Text($"Fiscal Year {tax.FiscalYear}").Italic();
                col.Item().LineHorizontal(1).LineColor(Colors.Grey.Lighten2);

                col.Item().PaddingTop(4).Text("INCOME SUMMARY").Bold().FontSize(12);
                AddLineRow(col, "Gross Livestock Sales", tax.TotalGrossSales, false);
                AddLineRow(col, "Other Farm Income", tax.TotalOtherIncome, false);
                AddLineRow(col, "Total Expenses", tax.TotalExpenses, false);
                AddLineRow(col, "Net Farm Income", tax.NetFarmIncome, true);

                col.Item().PaddingTop(6).Text("ESTIMATED TAX LIABILITY").Bold().FontSize(12);
                AddLineRow(col, "Self-Employment Tax (15.3%)", tax.SelfEmploymentTax, false);
                AddLineRow(col, "Estimated Federal Income Tax", tax.EstimatedFederalTax, false);
                AddLineRow(col, "Estimated State Income Tax", tax.EstimatedStateTax, false);
                AddLineRow(col, "Total Estimated Taxes",
                    tax.SelfEmploymentTax + tax.EstimatedFederalTax + tax.EstimatedStateTax, true);

                if (tax.ScheduleFItems.Count > 0)
                {
                    col.Item().PaddingTop(6).Text("SCHEDULE F MAPPING").Bold().FontSize(12);
                    foreach (var item in tax.ScheduleFItems)
                        col.Item().Row(row =>
                        {
                            row.RelativeItem().Text(item.LineDescription).FontSize(9);
                            row.ConstantItem(90).Text(item.Amount.ToString("C")).AlignRight().FontSize(9);
                        });
                }

                if (tax.CapitalGainEvents.Count > 0)
                {
                    col.Item().PaddingTop(6).Text("CAPITAL GAIN / LOSS EVENTS").Bold().FontSize(12);
                    foreach (var evt in tax.CapitalGainEvents)
                    {
                        col.Item().Text($"  {evt.AssetDescription} — " +
                            $"Acquired {evt.AcquisitionDate:MM/dd/yyyy}, " +
                            $"Sold {evt.SaleDate:MM/dd/yyyy} " +
                            $"({(evt.IsLongTerm ? "Long-Term" : "Short-Term")})");
                        col.Item().Row(row =>
                        {
                            row.RelativeItem().Text($"    Cost Basis: {evt.CostBasis:C}  " +
                                                    $"Sale Price: {evt.SalePrice:C}");
                            row.ConstantItem(90)
                               .Text((evt.GainOrLoss >= 0 ? "Gain: " : "Loss: ") +
                                     Math.Abs(evt.GainOrLoss).ToString("C"))
                               .AlignRight()
                               .FontColor(evt.GainOrLoss >= 0 ? Colors.Green.Darken3 : Colors.Red.Darken3);
                        });
                    }
                }
            });
            page.Footer().AlignCenter().Text(x =>
            {
                x.Span("Generated by Herd Master — ");
                x.Span(DateTime.Now.ToString("MM/dd/yyyy HH:mm"));
            });
        })).GeneratePdf(path);
    }

